package bean;

public class UserFace {
	private int id;
	private Users usersId;
	private String face;
	
	public UserFace() {
		super();
	}
	public UserFace(int id, Users usersId, String face) {
		super();
		this.id = id;
		this.usersId = usersId;
		this.face = face;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Users getUsersId() {
		return usersId;
	}
	public void setUsersId(Users usersId) {
		this.usersId = usersId;
	}
	public String getFace() {
		return face;
	}
	public void setFace(String face) {
		this.face = face;
	}
}
